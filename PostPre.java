public class PostPre {
    public static void main(String[] args){
        //Pre เครื่องหมายอยู่หน้า Post เครื่องหมายอยู่หลัง
        int a = 5;
        System.out.println(a);
        System.out.println(++a); //เพิ่มค่า

        int b = 10;
        System.out.println(b);
        System.out.println(--b); //ลดค่า
    }
}
